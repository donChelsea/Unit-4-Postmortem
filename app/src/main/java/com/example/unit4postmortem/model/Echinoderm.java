package com.example.unit4postmortem.model;

public class Echinoderm {
    private String animal;
    private String image;
    private String wiki;

    public Echinoderm(String animal, String image, String wiki) {
        this.animal = animal;
        this.image = image;
        this.wiki = wiki;
    }

    public java.lang.String getAnimal() {
        return animal;
    }

    public java.lang.String getImage() {
        return image;
    }

    public java.lang.String getWiki() {
        return wiki;
    }
}
